package com.David.week1;

public class Days {
    public static void main(String[] args) {
        int dayNumber = 1;
        if (dayNumber == 0) {
            System.out.println("Zondag");
        } else if (dayNumber == 1 ) {
            System.out.println("Maandag");
        } else if (dayNumber == 2) {
            System.out.println("Dinsdag");
        } else if (dayNumber == 3) {
            System.out.println("Woensdag");
        } else if (dayNumber == 4) {
            System.out.println("Donderdag");
        } else if (dayNumber == 5) {
            System.out.println("Vrijdag");
        } else if (dayNumber == 6) {
            System.out.println("Zaterdag");
        } else {
            System.out.println("Not a valid day.");
        }
        }
    }
