package com.David.week7.Aviary;

public class Bird {
    private String name;
    private String colour;
    private int age;
    private int weight;

    public Bird(String name, String colour, int age, int weight){
        this.name = name;
        this.colour = colour;
        this.age = age;
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
